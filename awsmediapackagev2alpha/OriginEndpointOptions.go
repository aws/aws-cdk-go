package awsmediapackagev2alpha

import (
	"github.com/aws/aws-cdk-go/awscdk/v2"
)

// Configuration parameters for an AWS Elemental MediaPackage V2 Origin Endpoint.
//
// Example:
//   var stack Stack
//
//
//   // Create a channel group
//   group := awsmediapackagev2alpha.NewChannelGroup(stack, jsii.String("MyChannelGroup"), &ChannelGroupProps{
//   	ChannelGroupName: jsii.String("my-channel-group"),
//   })
//
//   // Add a channel using the factory method
//   channel := group.addChannel(jsii.String("MyChannel"), &ChannelOptions{
//   	ChannelName: jsii.String("my-channel"),
//   	Input: awsmediapackagev2alpha.InputConfiguration_Cmaf(),
//   })
//
//   // Add an origin endpoint using the factory method
//   endpoint := channel.addOriginEndpoint(jsii.String("MyEndpoint"), &OriginEndpointOptions{
//   	OriginEndpointName: jsii.String("my-endpoint"),
//   	Segment: awsmediapackagev2alpha.Segment_Cmaf(),
//   	Manifests: []Manifest{
//   		awsmediapackagev2alpha.Manifest_Hls(&HlsManifestConfiguration{
//   			ManifestName: jsii.String("index"),
//   		}),
//   	},
//   })
//
// Experimental.
type OriginEndpointOptions struct {
	// Manifests configuration for HLS, Low Latency HLS and DASH.
	// Experimental.
	Manifests *[]Manifest `field:"required" json:"manifests" yaml:"manifests"`
	// The segment associated with the origin endpoint.
	//
	// Inside the segment configuration you can define options such as encryption, SPEKE parameters and other
	// general segment configurations.
	//
	// Use Segment.ts() or Segment.cmaf() to create the configuration.
	// Experimental.
	Segment *SegmentConfiguration `field:"required" json:"segment" yaml:"segment"`
	// Provide access to MediaPackage V2 Origin Endpoint via secret header.
	//
	// Use this when your CDN doesn't support AWS Signature Version 4 (SigV4)
	// authentication. For SigV4-based access with Amazon CloudFront, see
	// `MediaPackageV2Origin`.
	//
	// Setting `cdnAuth` auto-creates the following policy on the OriginEndpoint:
	//
	// ```json
	// {
	//   "Version":"2012-10-17",
	//   "Statement": [
	//     {
	//       "Sid": "AllowGetObjectAccessForAuthorizedRequest",
	//       "Effect": "Allow",
	//       "Principal": "*",
	//       "Action": "mediapackagev2:GetObject",
	//       "Resource": "arn:aws:mediapackagev2:us-east-1:111122223333:channelGroup/channelGroupName/channel/channelName/originEndpoint/originEndpointName",
	//       "Condition": {
	//         "Bool": {
	//           "mediapackagev2:RequestHasMatchingCdnAuthHeader": "true"
	//         }
	//       }
	//     }
	//   ]
	// }
	// ```.
	// See: https://docs.aws.amazon.com/mediapackage/latest/userguide/cdn-auth-setup.html
	//
	// Default: undefined - Not configured on endpoint.
	//
	// Experimental.
	CdnAuth *CdnAuthConfiguration `field:"optional" json:"cdnAuth" yaml:"cdnAuth"`
	// The description associated with the origin endpoint.
	// Default: undefined - No description is added to Origin Endpoint.
	//
	// Experimental.
	Description *string `field:"optional" json:"description" yaml:"description"`
	// The failover settings for the endpoint.
	// Default: undefined - No force endpoint configuration is configured.
	//
	// Experimental.
	ForceEndpointConfigurationConditions *[]EndpointErrorConfiguration `field:"optional" json:"forceEndpointConfigurationConditions" yaml:"forceEndpointConfigurationConditions"`
	// The name of the origin endpoint associated with the origin endpoint configuration.
	// Default: autogenerated.
	//
	// Experimental.
	OriginEndpointName *string `field:"optional" json:"originEndpointName" yaml:"originEndpointName"`
	// Policy to apply when the origin endpoint is removed from the stack.
	//
	// Even though MediaPackage ChannelGroups, Channels and OriginEndpoints are technically stateful,
	// their contents are transient and it is common to add and remove these while rearchitecting your application.
	// The default is therefore `DESTROY`. Change it to `RETAIN` if the content (in a lookback window) are so
	// valuable that accidentally losing it would be unacceptable.
	// Default: RemovalPolicy.DESTROY
	//
	// Experimental.
	RemovalPolicy awscdk.RemovalPolicy `field:"optional" json:"removalPolicy" yaml:"removalPolicy"`
	// The size of the window to specify a window of the live stream that's available for on-demand viewing.
	//
	// Viewers can start-over or catch-up on content that falls within the window.
	// Default: 900.
	//
	// Experimental.
	StartoverWindow awscdk.Duration `field:"optional" json:"startoverWindow" yaml:"startoverWindow"`
	// The tags associated with the origin endpoint.
	// Default: - No tagging.
	//
	// Experimental.
	Tags *map[string]*string `field:"optional" json:"tags" yaml:"tags"`
}

